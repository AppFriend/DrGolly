export async function seedFreebies() {
  try {
    console.log("Seeding freebies blog posts...");
    
    for (const freebie of freebiesData) {
      await db.insert(blogPosts).values(freebie).onConflictDoNothing();
      console.log(`✓ Seeded: ${freebie.title}`);
    }
    
    console.log("✅ Successfully seeded all freebies blog posts!");
  } catch (error) {
    console.error("❌ Error seeding freebies:", error);
    throw error;
  }
}

// Run if called directly
